# 제목: DFS (깊이 우선 탐색) 재귀 기본 템플릿

# DFS는 그래프의 모든 정점을 탐색하는 알고리즘으로, 한 경로를 끝까지 파고든 후
# 되돌아와 다른 경로를 탐색하는 방식입니다. 재귀로 구현하면 코드가 간결해집니다.

def dfs(graph, v, visited):
    # 현재 노드를 방문 처리
    visited[v] = True
    print(v, end=' ')

    # 현재 노드와 연결된 다른 노드를 재귀적으로 방문
    for i in graph[v]:
        if not visited[i]:
            dfs(graph, i, visited)

# 각 노드가 연결된 정보를 리스트 자료형으로 표현 (인접 리스트)
# 1번 노드부터 시작한다고 가정하고, 0번 인덱스는 비워둠
graph = [
    [],
    [2, 3, 8],
    [1, 7],
    [1, 4, 5],
    [3, 5],
    [3, 4],
    [7],
    [2, 6, 8],
    [1, 7]
]

# 각 노드가 방문된 정보를 리스트 자료형으로 표현
visited = [False] * 9

# 정의된 DFS 함수 호출
print("DFS 방문 순서:")
dfs(graph, 1, visited)