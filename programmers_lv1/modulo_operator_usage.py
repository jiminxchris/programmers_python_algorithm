# 제목: 나머지 연산자 (%) 활용 (짝수/홀수, 배수 찾기)

# 나머지 연산자(%)는 한 숫자를 다른 숫자로 나누었을 때의 나머지를 구해줍니다.
# 다양한 알고리즘 문제에서 유용하게 사용됩니다.

# 1. 짝수 / 홀수 판별
# 어떤 수를 2로 나누었을 때 나머지가 0이면 짝수, 1이면 홀수입니다.
num = 10
if num % 2 == 0:
    print(f"{num}은(는) 짝수입니다.")
else:
    print(f"{num}은(는) 홀수입니다.")

num = 7
if num % 2 == 0:
    print(f"{num}은(는) 짝수입니다.")
else:
    print(f"{num}은(는) 홀수입니다.")


# 2. 특정 수의 배수 찾기
# 1부터 20까지의 숫자 중 3의 배수만 출력하기
print("\n1부터 20까지의 3의 배수:")
for i in range(1, 21):
    # i를 3으로 나누었을 때 나머지가 0이면 3의 배수
    if i % 3 == 0:
        print(i, end=' ')
print()