# 제목: 비교 연산자 (==, !=, >, <, >=, <=)

# 비교 연산자는 두 값을 비교하여 그 결과를 참(True) 또는 거짓(False)으로 알려줍니다.
# if문과 함께 자주 사용됩니다.

a = 10
b = 5

# 같은지 비교 (==)
print(f"{a} == {b} -> {a == b}")

# 다른지 비교 (!=)
print(f"{a} != {b} -> {a != b}")

# 큰지 비교 (>)
print(f"{a} > {b} -> {a > b}")

# 작은지 비교 (<)
print(f"{a} < {b} -> {a < b}")

# 크거나 같은지 비교 (>=)
print(f"{a} >= 10 -> {a >= 10}")

# 작거나 같은지 비교 (<=)
print(f"{b} <= 10 -> {b <= 10}")