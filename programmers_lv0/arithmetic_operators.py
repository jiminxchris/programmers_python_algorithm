# 제목: 기본 산술 연산자 (+, -, *, /, %)

# 숫자 계산에 사용되는 기본적인 연산자들입니다.

a = 10
b = 3

# 덧셈 (+)
add_result = a + b
print(f"{a} + {b} = {add_result}")

# 뺄셈 (-)
sub_result = a - b
print(f"{a} - {b} = {sub_result}")

# 곱셈 (*)
mul_result = a * b
print(f"{a} * {b} = {mul_result}")

# 나눗셈 (/) - 결과가 항상 실수(float)로 나옵니다.
div_result = a / b
print(f"{a} / {b} = {div_result}")

# 나눗셈의 몫 (//)
quotient = a // b
print(f"{a}을(를) {b}로 나눈 몫: {quotient}")

# 나머지 (%)
remainder = a % b
print(f"{a}을(를) {b}로 나눈 나머지: {remainder}")