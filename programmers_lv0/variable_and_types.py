# 제목: 변수 선언과 기본 자료형 (정수, 문자열)

# 변수는 데이터를 담는 상자라고 생각하면 쉬워요.
# age라는 상자에 숫자 10을 담고, name이라는 상자에 문자열 "Alice"를 담는 과정입니다.

# 정수 (Integer)형 변수 선언
age = 10

# 문자열 (String)형 변수 선언
name = "Alice"

# 변수에 담긴 값 출력해보기
print(age)
print(name)

# type() 함수를 사용하면 변수에 어떤 종류의 데이터가 들어있는지 확인할 수 있습니다.
print(type(age))
print(type(name))