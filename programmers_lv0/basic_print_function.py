# 제목: print() 함수로 화면에 결과 출력하기

# print()는 코드의 실행 결과를 눈으로 확인하기 위한 가장 기본적인 함수입니다.

name = "Bob"
age = 25

# 단순한 텍스트 출력
print("Hello, Python!")

# 변수에 저장된 값 출력
print(name)
print(age)

# 여러 값을 쉼표(,)로 구분하여 한 번에 출력하기
# 쉼표로 구분하면 값들 사이에 자동으로 띄어쓰기가 들어갑니다.
print("이름:", name, ", 나이:", age)

# f-string을 사용하면 변수와 문자열을 더 깔끔하게 조합하여 출력할 수 있습니다. (추천)
print(f"이름: {name}, 나이: {age}")